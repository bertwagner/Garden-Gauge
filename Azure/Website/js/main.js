var LoadData = function(sensorName,sensorId,startDate,startHour,stopDate,stopHour) {
    var url = `https://gardengauge.azurewebsites.net/api/ReadSensor5min?sensor_name=${sensorName}&sensor_id=${sensorId}&start_date=${startDate}&start_hour=${startHour}&stop_date=${stopDate}&stop_hour=${stopHour}`;
    console.log(url);
    fetch(url).then(function (response) {
        if (response.ok) {
            return response.json();
        } else {
            return Promise.reject(response);
        }
    }).then(function (data) {
        //var prettyData = JSON.stringify(data,null,2);
        //document.querySelector("#data1").append(prettyData);
        //var labels = data.map(function(entry){})
        var labels = data.map(function(entry){
            return entry["Timestamp"];
        });
        var dataset = data.map(function(entry){
            return entry["Value"];
        });

        config.data.labels = labels;
        config.data.datasets[0].data = dataset;

        console.log("Labels: ",config.data.labels);
        console.log("Data: ",config.data.datasets[0].data);
        chart.update();
    }).catch(function (err) {
        console.warn('Fetch error: ', err);
    });
}

